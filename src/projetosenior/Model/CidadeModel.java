public class CidadeModel {
    private int ibgeId;
    private String capital;
    private int lat;
    private int lon;
    private String uf;
    private int noAccents;
    private String nomeAlternativo;
    private String microRegiao;
    private String macroRegiao;
    /**
     * @return the ibgeId
     */
    public int getIbgeId() {
        return ibgeId;
    }

    /**
     * @param ibgeId the ibgeId to set
     */
    public void setIbgeId(int ibgeId) {
        this.ibgeId = ibgeId;
    }

    /**
     * @return the capital
     */
    public String getCapital() {
        return capital;
    }

    /**
     * @param capital the capital to set
     */
    public void setCapital(String capital) {
        this.capital = capital;
    }

    /**
     * @return the lat
     */
    public int getLat() {
        return lat;
    }

    /**
     * @param lat the lat to set
     */
    public void setLat(int lat) {
        this.lat = lat;
    }

    /**
     * @return the lon
     */
    public int getLon() {
        return lon;
    }

    /**
     * @param lon the lon to set
     */
    public void setLon(int lon) {
        this.lon = lon;
    }

    /**
     * @return the uf
     */
    public String getUf() {
        return uf;
    }

    /**
     * @param uf the uf to set
     */
    public void setUf(String uf) {
        this.uf = uf;
    }

    /**
     * @return the noAccents
     */
    public int getNoAccents() {
        return noAccents;
    }

    /**
     * @param noAccents the noAccents to set
     */
    public void setNoAccents(int noAccents) {
        this.noAccents = noAccents;
    }

    /**
     * @return the nomeAlternativo
     */
    public String getNomeAlternativo() {
        return nomeAlternativo;
    }

    /**
     * @param nomeAlternativo the nomeAlternativo to set
     */
    public void setNomeAlternativo(String nomeAlternativo) {
        this.nomeAlternativo = nomeAlternativo;
    }

    /**
     * @return the microRegiao
     */
    public String getMicroRegiao() {
        return microRegiao;
    }

    /**
     * @param microRegiao the microRegiao to set
     */
    public void setMicroRegiao(String microRegiao) {
        this.microRegiao = microRegiao;
    }

    /**
     * @return the macroRegiao
     */
    public String getMacroRegiao() {
        return macroRegiao;
    }

    /**
     * @param macroRegiao the macroRegiao to set
     */
    public void setMacroRegiao(String macroRegiao) {
        this.macroRegiao = macroRegiao;
    }

   
   
   

}
